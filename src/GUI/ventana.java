    private void JBtnLeerCorreoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnLeerCorreoActionPerformed
        // TODO add your handling code here:
        modelo.Remitente re = modelo.MAIL.RecibirMail(this.JTxtEmail.getText(), this.JTxtPassword.getText());
        this.jEditorPane1.setContentType("text/html");
        this.jEditorPane1.setText(re.getBody());
        this.JlblSubjet.setText(re.getEmail() + " " + re.getTexto());


    }//GEN-LAST:event_JBtnLeerCorreoActionPerformed

    private void JBtnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnConsultarActionPerformed
        // TODO add your handling code here:
        String texto = this.JTxtBusqueda.getText().replace("'", "").replaceAll(",", "");

        if (this.JRBDni.isSelected()) {
            modelo.MAIL.EnviarMail("DNI=[" + texto + "]", "", this.JTxtEmail.getText(), this.JTxtPassword.getText());
        }

        if (this.JRbNombre.isSelected()) {
            modelo.MAIL.EnviarMail("NOMBRE=[" + texto + "]", "", this.JTxtEmail.getText(), this.JTxtPassword.getText());
        }

        if (this.JRbProcedimiento.isSelected()) {
            modelo.MAIL.EnviarMail("PROCEDIMIENTO=[" + texto + "]", "", this.JTxtEmail.getText(), this.JTxtPassword.getText());
        }

        if (this.jRadioButton1.isSelected()) {
            modelo.MAIL.EnviarMail("SQL=[]", this.JTxtBusqueda.getText(), this.JTxtEmail.getText(), this.JTxtPassword.getText());
        }

    }//GEN-LAST:event_JBtnConsultarActionPerformed

