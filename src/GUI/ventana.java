    private void JTBtnMostarMailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JTBtnMostarMailActionPerformed
        // TODO add your handling code here:
        if (this.JTBtnMostarMail.isSelected()) {

            this.JTBtnMostarMail.setText("Ocultar Conf Email");
            this.JTxtEmail.setForeground(Color.BLACK);
            this.JTxtPassword.setForeground(Color.BLACK);

        } else {
            this.JTBtnMostarMail.setText("Mostrar Conf Email");

            this.JTxtEmail.setForeground(Color.WHITE);
            this.JTxtPassword.setForeground(Color.WHITE);
        }


    }//GEN-LAST:event_JTBtnMostarMailActionPerformed

